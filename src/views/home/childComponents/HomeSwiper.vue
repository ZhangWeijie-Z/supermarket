<template>
  <swiper>
    <swiper-item v-for='item in banners'>
      <a :href='item.link'>
        <img :src='item.image' alt='' @load='imageLoad'>
      </a>
    </swiper-item>
  </swiper>
</template>
<script>
import {Swiper, SwiperItem} from "components/common/swiper"

export default {
  name: "HomeSwiper",
  props: {
    banners: {
      type: Array,
      default() {
        return []
      }
    }
  },
  data() {
    return {
      isSend: true
    }
  },
  components: {
    Swiper,
    SwiperItem
  },
  methods: {
    imageLoad() {
      if (this.isSend) {
        this.$emit('SwiperImgLoad')
        this.isSend = false
      }
    }
  }
}
</script>
